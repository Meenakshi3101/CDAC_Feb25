
class Calculator{
public static void main(String[] args){
int num1 = 40;
int num2 = 20;

String n = "+";

if(n == "+"){
         System.out.println(num1+num2);

}else if(n == "-"){
        System.out.println(num1-num2);

}else if(n == "*"){
         System.out.println(num1*num2);

}else if(n == "/"){
        System.out.println(num1/num2);

}else{
      System.out.println("Invalid");
}
}
}




















